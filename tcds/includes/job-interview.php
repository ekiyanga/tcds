    	<h2>Job interview Skills</h2>
         <p>Preparing for the interview does not come, and you should not do it after you exit the college or after you get shortlisted. It should rather go hand in hand with your schooling. In this package, we teach you some advanced skills that will make you perform well in modern job interview sessions.</p>
		<p>We begin by encouraging our youngsters to read job adverts in your specializations during your schooling and before you start job search. If possible, you would benefit more from taking screenshots of the job adverts for the positions you aspire for and archive them for future analysis before you start hunting the job vacancies.</p>
		<p>We have dedicated chapter Four to train you on skills how job interview skills in the book “The Future of Work: Preparing for the Work that Does Not Exist Yet”. You may order an eBook version or a hard copy through our online shop available at www.tcds.or.tz .</p>
		<p>In the chapter, we discuss the importance of:</p>
		<p>1.     Reading the job advertisement thoroughly.</p>
		<p>2.     Background checking the organization you want to apply for job.</p>
		<p>3.     Commonly asked interview questions such as;</p>
		<p>a.     Tell us about yourself and why you applied for this job?</p>
		<p>b.     What are your strengths?</p>
		<p>c.      Other technical skills questions for specific specializations.</p>
		<p>4.     Managing your first two months at your new job.</p>
