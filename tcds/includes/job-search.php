		<h2>Job Search & Application Skills</h2>
    	<p>This package composes some tips and tricks to optimize job search skills.</p>
		<p>For the born before computer generation, walking to search for jobs was a common practice. But the same search skills cannot be efficient method in the high-tech society which we live in. Such traditional skills won’t prevail in the future world where almost every employment service will be digitalized. Technological evolution shapes job searches. Hence, there are search skills relevant to every development level of technological evolution.</p>
		<p>The job search skills we offer will help you search for jobs by combining different strategies ranging from the traditional to the modern ones. To make informed decisions on what job to apply, you need to have correct information about the labour market.</p>
		<h2><strong>Labour Market Information (LMI)</strong></h2>
		<p>Labour market information (sometimes also called labour market intelligence) includes reports, studies, statistics and other information about:</p>
		<p>a)    Industry / sector growth and decline.</p>
		<p>b)    Occupational information.</p>
		<p>c)     Employment rates.</p>
		<p>d)    Supply and demand for labour.</p>
		<p>e)    Future labour trends.</p>
		<p>f)     Wages and pay rates.</p>
		<p>LMI will help you:</p>
		<p>a)    Identify the sectors where there are growth and the possibility of jobs. Don’t just look at the current situation but use LMI trends to identify where the jobs may be in the future.</p>
		<p>b)   Show potential employers your understanding of the environment in which they operate. Many employers comment on candidates’ lack of knowledge about the sector or industry to which they are applying. And researching the job and the individual company, make sure you know what issues the sector is facing, who are the major organizations in the sector, and what the future might hold for it</p>
