<h2>Mission</h2>

<div class="row">
	<div class='col-md-12'>
		<p><strong><em>Our Mission:</em></strong>
		<strong>Bridging the Gap between Education System and Labour Market</strong>
		<p>Experiences show that many graduates struggle to find jobs for a substantial period. Even if they do, they struggle to match the skills they acquired from the education system to the working environment. During this time of struggle, the victims experience a lot of psychological trauma, stress, anxiety, fear, frustrations, and underperformance. Some end up losing their jobs or spend a lot of time searching for a different job that might help them achieve their goals. Employers spend time and money recruiting new employees to fill the vacancies. Thus, there is a lot of energy and time being wasted. This increases the unemployment of the young Tanzanians. If this time andenergy could be saved, it could add value to productivity.</p>
		<p>We will provide every young Tanzanian with skills to effectively achieve their goals within a specified period. We will</p>
		<ol>
			<li>Help them identify and plan to acquire the skills needed in their professions before entering the job market.</li>
			<li>Develop skills thatare required to succeed in the future of work such as creativity, innovative and problem-solving.</li>
			<li>Build their skills to overcome financial obstacles that might hinder them to achieve their goals.</li>
			<li>Impart them with skills to solve real-life problems.</li>
			<li>Prepare them to do the jobs that Never Existed.</li>
			<li>Help them manage their career growth and lives.</li>
		</ol>
		<p>Apart from that we will provide Age appropriate Career Development Services starting from Early years of life from which children will be exposed to Age appropriate Careers. Also we will complement key skills needed for 21<sup>st</sup> C Labour Market including Soft skills.</p>
	</div>
</div>	

