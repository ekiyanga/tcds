		<h2>Scholarships Application Skills</h2>
    	<p>In case you cannot secure a loan from commercial banks or HESLB, you can still finance your studies through scholarships. These are financial assistance provided by different organisations in the country or even from another one. For them, their financial assistance is free: you will not be required to repay it. However, you do have to show them that you are worthy of being awarded a scholarship.</p>
		<p>We have dedicated a whole chapter to teach you skills on how you can apply and win a scholarship in our book called “The Future of Work: Preparing for the Work that Does Not Exist Yet”. You may peruse our website at www.tcds.or.tz to order an eBook or a hardcopy of the book.</p>
		<p>In Chapter 3 of the book, is dedicated to “How to Apply For and Win a Scholarship”, we talk about:</p>
